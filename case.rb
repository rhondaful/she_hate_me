weather = 'clear'

case weather
    when 'sunny'
    puts "Get out the tank tops! We're taking the camper off the truck!"
    when 'cloudy'
    puts "Uhg, You'd might as well do some house work."
    when 'raining'
    puts "Rain is always welcomed around here. We don't get much."
    when 'iced'
    puts "Better stay home. You know them people can't drive on black ice?"
    when 'clear'
    puts "The Stars are big and bright, deep in the heart of Texas!"
    when 'snowing'
    puts "Now what part of Texas are you in, exactly?"
    else
    puts "Well it is Texas. Give it a minute, it'll change."
end
